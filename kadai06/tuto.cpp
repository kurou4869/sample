void DrawTuto(void)
{
	PLAYER *player = GetPlayerAdr(0);
	int y;
	char tuto[TATE_MAX][FW_MAX + 1] = { // 横を+1しているのは文末のNULLの分
		"*******************************************************************************",
		"**                                                                           **",
		"**                              チュートリアル                               **",
		"**                                                                           **",
		"*******************************************************************************",
		"*                                                                             *",
		"*      1:ENTERキーの連打はやめてください演出が飛ぶかもしれません。            *",
		"*                                                                             *",
		"*      2:敵を倒して最後のボスに挑むだけのゲームです。                         *",
		"*                                                                             *",
		"*      3:ストーリーなどはありません。                                         *",
		"*                                                                             *",
		"*      4:プレイヤーの移動は「カーソルキー」と「W、A、S、D」です。             *",
		"*                                                                             *",
		"*                                                                             *",
		"*                                                                             *",
		"*                                 PRESS ENTER                                 *",
		"*                                                                             *",
		"*                                                                             *",
		"*****************                                             *****************",
		"*****************                                             *****************",
		"*****************                                             *****************",
		"*****************                                             *****************",
		"*******************************************************************************",
	};

	/* 画面描画処理 */
	for (y = 0; y < FH_MAX; y++)
	{
		printf("%s\n", &tuto[y][0]);
	}

	//キー入力待ち
	rewind(stdin);
	_getch();

	system("cls");

	printf("                     始める前に君の名前を教えてください！\n");
	printf("                     注意 9文字で入力してくださいー＞");
	scanf("%s", &player->name[0]);
	printf("\n");
	printf("                     では%9s BATTLEの時間だ！！\n", &player->name[0]);


	



	

}