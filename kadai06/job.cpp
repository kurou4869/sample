JOB job[JOB_MAX];



/*******************************************************************************
関数名:	void InitJob( void )
引数:	void
戻り値:	void
説明:	jobの初期化処理
*******************************************************************************/
void InitJob(void)
{
	JOB *pjob = GetJobAdr(0);

	//職業別パラメーター
	strcpy(&pjob->status.jobname[0], "勇者");
	pjob->status.HP = 100;
	pjob->status.MP = 10;
	pjob->status.ATK = 50;
	pjob->status.DEF = 50;

	pjob++;

	strcpy(&pjob->status.jobname[0], "魔法使い");
	pjob->status.HP = 60;
	pjob->status.MP = 55;
	pjob->status.ATK = 10;
	pjob->status.DEF = 15;

	pjob++;

	strcpy(&pjob->status.jobname[0], "パラディン");
	pjob->status.HP = 110;
	pjob->status.MP = 17;
	pjob->status.ATK = 55;
	pjob->status.DEF = 55;

	pjob++;

	strcpy(&pjob->status.jobname[0], "盗賊");
	pjob->status.HP = 90;
	pjob->status.MP = 13;
	pjob->status.ATK = 45;
	pjob->status.DEF = 43;



}




/*******************************************************************************
関数名:	ENEMY *GetEnemyAdr( void )
引数:	int eno
戻り値:	ENEMY*
説明:	エネミーのアドレスを取得する
*******************************************************************************/
JOB *GetJobAdr(int jno)
{
	return &job[jno];
}
